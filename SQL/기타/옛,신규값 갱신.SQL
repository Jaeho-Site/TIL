WITH player_table AS (
    SELECT 101 AS player_id, 2400 AS prev_score UNION ALL
    SELECT 102, 2000 UNION ALL
    SELECT 103, 2000 UNION ALL
    SELECT 104, 1500 UNION ALL
    SELECT 105, 1700
),
tournament_table AS (
    -- current_rank는 DENSE_RANK 형식
    SELECT 102 AS player_id, 1 AS current_rank UNION ALL
    SELECT 106, 1 AS current_rank UNION ALL
    SELECT 101, 2 AS current_rank UNION ALL
    SELECT 103, 3 AS current_rank UNION ALL
    SELECT 104, 4 AS current_rank
),
-- 여기까지가 예제 테이블 (아래부터 정답 쿼리)

PREV_RANK AS(
    SELECT PLAYER_ID,PREV_SCORE,
    DENSE_RANK() OVER(ORDER BY PREV_SCORE DESC) AS PREV_RANK
    FROM player_table
)

SELECT PLAYER_ID,UPDATED_SCORE
FROM(
SELECT T.PLAYER_ID,
IFNULL(P.PREV_SCORE,1600)+(25*(IFNULL(P.PREV_RANK,T.CURRENT_RANK)-T.CURRENT_RANK)) AS UPDATED_SCORE
FROM tournament_table T
LEFT JOIN PREV_RANK P ON T.PLAYER_ID=P.PLAYER_ID

UNION

SELECT PLAYER_ID,PREV_SCORE AS UPDATED_SCORE
FROM PREV_RANK
WHERE PLAYER_ID NOT IN (SELECT PLAYER_ID FROM tournament_table)
) AS RESULT
ORDER BY UPDATED_SCORE DESC;