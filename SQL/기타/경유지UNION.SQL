WITH USER_REQUESTS AS (
    SELECT 1 AS REQUEST_ID, 'SEOUL' AS ORIGIN, 'JEJU' AS DESTINATION UNION ALL
    SELECT 2,'SEOUL','TOKYO' UNION ALL
    SELECT 3,'TOKYO','LONDON' UNION ALL
    SELECT 4,'JEJU','ROMA' UNION ALL
    SELECT 5,'ROMA','SIDNY' 
),
FLIGHT AS(
    SELECT 1 AS FLIGHT_ID, 'SEOUL' AS ORIGIN, 'JEJU' AS DESTINATION, 200 AS DURATION_TIME, 70 AS COST UNION ALL
    SELECT 2 , 'SEOUL', 'JEJU', 100, 100 UNION ALL
    SELECT 3 , 'SEOUL', 'TOKYO', 200 , 140 UNION ALL
    SELECT 4, 'JEJU' , 'TOKYO', 50 , 50 UNION ALL
    SELECT 5 , 'TOKYO', 'LONDON', 500, 900 UNION ALL
    SELECT 6 , 'LONDON' ,'ROMA', 30, 60
),

-- 1. 항공편 테이블을 환승내역까지 만든다. (경유는 2번이 최대)
T AS(
    SELECT F1.ORIGIN AS ORIGIN, F2.DESTINATION AS DESTINATION,
    F1.DURATION_TIME+F2.DURATION_TIME AS DURATION_TIME,
    F1.COST + F2.COST AS COST
    FROM FLIGHT F1
    JOIN FLIGHT F2 ON F1.DESTINATION = F2.ORIGIN

    UNION ALL

    SELECT ORIGIN,DESTINATION,DURATION_TIME,COST
    FROM FLIGHT
)

SELECT 
    R.REQUEST_ID,
    MIN(IFNULL(T.DURATION_TIME,-1)) AS MIN_DURATION_TIME, 
    MIN(IFNULL(T.COST,-1)) AS MIN_COST
FROM USER_REQUESTS R
LEFT JOIN T ON R.ORIGIN=T.ORIGIN AND R.DESTINATION=T.DESTINATION
GROUP BY R.REQUEST_ID
ORDER BY R.REQUEST_ID DESC