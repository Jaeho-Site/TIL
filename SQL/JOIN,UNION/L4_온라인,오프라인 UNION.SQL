-- ONLINE_SALE : ONLINE_SALE_ID, USER_ID, PRODUCT_ID, SALES_AMOUNT, SALES_DATE
-- OFFLINE_SALE : OFFLINE_SALE_ID, PRODUCT_ID, SALES_AMOUNT, SALES_DATE
/*
판매 날짜, 상품ID, 유저ID, 판매량
1. 2022년 3월
2. OFFLINE_SALE 테이블의 판매 데이터의 USER_ID 값은 NULL 로 표시
*/
SELECT DATE_FORMAT(SALES_DATE, '%Y-%m-%d') AS SALES_DATE,
PRODUCT_ID,USER_ID,SALES_AMOUNT
FROM (
    SELECT SALES_DATE,PRODUCT_ID,USER_ID,SALES_AMOUNT
    FROM ONLINE_SALE WHERE SALES_DATE LIKE '2022-03%'

    UNION ALL

    SELECT SALES_DATE,PRODUCT_ID,NULL AS USER_ID,SALES_AMOUNT
    FROM OFFLINE_SALE WHERE SALES_DATE LIKE '2022-03%'
)AS T
ORDER BY SALES_DATE,PRODUCT_ID,USER_ID