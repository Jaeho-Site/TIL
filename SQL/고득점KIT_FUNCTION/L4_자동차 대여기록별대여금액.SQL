-- CAR : CAR_ID, CAR_TYPE, DAILY_FEE
-- 랜탈 : HISTORY_ID, CAR_ID, START_DATE, END_DATE
-- 플랜 : PLAN_ID, CAR_TYPE, DURATION_TYPE, DISCOUNT_RATE
/*
자동차 종류가 '트럭'
대여 기록 별로 대여 금액
*/
WITH T AS(
SELECT H.HISTORY_ID, C.DAILY_FEE,
    CASE 
        WHEN DATEDIFF(H.END_DATE, H.START_DATE)+1 >=90 THEN '90일 이상'
        WHEN DATEDIFF(H.END_DATE, H.START_DATE)+1 BETWEEN 30 AND 89 THEN '30일 이상'
        WHEN DATEDIFF(H.END_DATE, H.START_DATE)+1 BETWEEN 7 AND 29 THEN '7일 이상'
        ELSE 'NO'
    END AS DIFF,
    DATEDIFF(H.END_DATE, H.START_DATE)+1 AS D_COUNT
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY H
JOIN CAR_RENTAL_COMPANY_CAR C ON H.CAR_ID=C.CAR_ID 
AND C.CAR_TYPE='트럭'
)

SELECT T.HISTORY_ID,
FLOOR(T.DAILY_FEE*T.D_COUNT*(1-IFNULL(P.DISCOUNT_RATE,0)/100)) AS FEE
FROM T
LEFT JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P ON T.DIFF=P.DURATION_TYPE
AND P.CAR_TYPE = '트럭'
ORDER BY FEE DESC,T.HISTORY_ID DESC