-- CAR            : CAR_ID, CAR_TYPE, DAILY_FEE, OPTIONS
-- RENTAL_HISTORY : HISTORY_ID, CAR_ID, START_DATE, END_DATE
-- DISCOUNT_PLAN  : PLAN_ID, CAR_TYPE, DURATION_TYPE, DISCOUNT_RATE
/*
자동차 ID, 자동차 종류, 대여 금액(컬럼명: FEE) 최종 출력
1. 자동차 종류가 '세단' 또는 'SUV' 인 자동차 
2. 2022년 11월 1일부터 2022년 11월 30일까지 대여 가능
3. 30일간의 대여 금액이 50만원 이상 200만원 미만
*/
SELECT C.CAR_ID, C.CAR_TYPE,
       FLOOR((C.DAILY_FEE * 30)*((100-D.DISCOUNT_RATE)/100)) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN D ON C.CAR_TYPE = D.CAR_TYPE 
AND D.DURATION_TYPE = '30일 이상'
WHERE C.CAR_TYPE IN ('세단','SUV') -- 1번 조건 
  AND CAR_ID NOT IN (
    SELECT DISTINCT CAR_ID  -- 2번 조건
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE START_DATE <= '2022-11-30' AND END_DATE >= '2022-11-01'
  )
  AND FLOOR((C.DAILY_FEE * 30)*((100-D.DISCOUNT_RATE)/100)) BETWEEN 500000 AND 1999999
ORDER BY FEE DESC, C.CAR_TYPE ASC, C.CAR_ID DESC

-- 대여 금액이란걸 구해야함. -> CAR의 하루금액 * 30 이 범위안, 할인율까지