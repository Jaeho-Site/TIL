/*
내 코드 오류 이유
1. 대여 가능 여부 판별의 논리 오류 : 
NOT EXISTS나 CAR_ID NOT IN 서브쿼리
2. JOIN 방식에 따른 중복 발생 :
HISTORY 테이블을 직접 JOIN하면, 한 자동차에 여러 개의 대여 기록이
있을 경우 결과 테이블에 동일한 자동차가 여러 번 등장하게 된다.
3. 할인 정책(DISCOUNT_PLAN) 선택 조건 누락 :
'7일 이상', '30일 이상', '90일 이상' 등 여러 할인 조건
*/

-- 정답 풀이
SELECT 
    C.CAR_ID, 
    C.CAR_TYPE, 
    FLOOR(C.DAILY_FEE * 30 * (1 - P.DISCOUNT_RATE / 100)) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P 
    ON C.CAR_TYPE = P.CAR_TYPE AND P.DURATION_TYPE = '30일 이상' -- 30일 할인만 조인
WHERE C.CAR_TYPE IN ('세단', 'SUV')
  AND C.CAR_ID NOT IN (
      -- 11월 1일부터 11월 30일 사이에 대여 기록이 있는 차 ID 추출
      SELECT CAR_ID 
      FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY 
      WHERE END_DATE >= '2022-11-01' AND START_DATE <= '2022-11-30'
  )
  AND FLOOR(C.DAILY_FEE * 30 * (1 - P.DISCOUNT_RATE / 100)) BETWEEN 500000 AND 1999999
ORDER BY FEE DESC, CAR_TYPE ASC, CAR_ID DESC;


-- 내 풀이

-- CAR_RENTAL_COMPANY_CAR : CAR_ID, CAR_TYPE, DAILY_FEE, OPTIONS
-- CAR_RENTAL_COMPANY_RENTAL_HISTORY : HISTORY_ID, CAR_ID, START_DATE, END_DATE
-- CAR_RENTAL_COMPANY_DISCOUNT_PLAN : PLAN_ID, CAR_TYPE, DURATION_TYPE, DISCOUNT_RATE
/*
1. 자동차 종류가 '세단' 또는 'SUV' 인 자동차 
2. 2022년 11월 1일부터 2022년 11월 30일까지 대여 가능
3. 30일간의 대여 금액이 50만원 이상 200만원 미만
*/
SELECT 
    C.CAR_ID,
    C.CAR_TYPE,
    FLOOR((C.DAILY_FEE * ((100-P.DISCOUNT_RATE)/100))*30) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C
JOIN CAR_RENTAL_COMPANY_RENTAL_HISTORY H ON C.CAR_ID=H.CAR_ID
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P ON C.CAR_TYPE=P.CAR_TYPE
WHERE
    (C.CAR_TYPE = 'SUV' OR C.CAR_TYPE = '세단')  AND
    NOT (H.END_DATE BETWEEN '2022-11-01' AND '2022-11-30') AND
    FLOOR((C.DAILY_FEE * ((100-P.DISCOUNT_RATE)/100))*30) BETWEEN 500000 AND 2000000
ORDER BY FEE DESC, C.CAR_TYPE ASC, C.CAR_ID DESC