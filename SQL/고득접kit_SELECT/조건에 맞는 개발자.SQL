/* SKILLCODES,DEVELOPERS
Python이나 C# 스킬을 가진 개발자의 ID, 이메일, 이름, 성을 조회
2 4 7 8 10 11 15 16
      P C#
*/
-- 내 풀이처럼 풀면 안됨
SELECT DISTINCT D.ID, D.EMAIL,D.FIRST_NAME,D.LAST_NAME
FROM DEVELOPERS D
JOIN SKILLCODES S ON D.SKILL_CODE = S.CODE
WHERE S.NAME IN ('Python','C#')
ORDER BY ID ASC

-- 정답 풀이 비트연산
SELECT DISTINCT D.ID, D.EMAIL, D.FIRST_NAME, D.LAST_NAME
FROM DEVELOPERS D
JOIN SKILLCODES S ON (D.SKILL_CODE & S.CODE) = S.CODE  -- 비트 포함 여부 확인
WHERE S.NAME IN ('Python', 'C#')
ORDER BY D.ID ASC;