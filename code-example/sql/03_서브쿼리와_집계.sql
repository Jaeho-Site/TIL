-- ---------------------------------------------------
-- [문제 1] GROUP BY + AS + HAVING (2023-1 유형 변형)
-- HAVING은 "그룹에 대한 조건" (개별 행 조건은 WHERE)
--
-- <사원>
--  부서  급여        부서별로 묶으면:
--  영업  200         영업: MIN 200, MAX 400, AVG 300 → 통과
--  영업  400         개발: MIN 300, MAX 500, AVG 400 → 통과
--  개발  500         총무: MIN 150, MAX 250, AVG 200 → 탈락
--  개발  300
--  총무  150         ★ AS 별칭을 썼으므로 결과 헤더는 최저급여/최고급여
--  총무  250
-- ---------------------------------------------------
SELECT 부서, MIN(급여) AS 최저급여, MAX(급여) AS 최고급여
FROM 사원
GROUP BY 부서                -- ① GROUP BY
HAVING AVG(급여) >= 300;     -- ② HAVING
-- 답:  부서  최저급여  최고급여
--      영업  200      400
--      개발  300      500

-- ---------------------------------------------------
-- [문제 2] IN 서브쿼리 결과 (2024-1 유형 변형)
-- "출력되는 속성명과 값을 모두 쓰시오" - 속성명도 답이다!
--
-- <R1>               <R2>
--  A  B  C            C  D
--  1  w  g            g  p
--  2  h  m            m  q
--  3  d  n            n  p
--
-- ① 안쪽: R2에서 D='p'인 C → { g, n }
-- ② 바깥: R1에서 C가 g 또는 n인 행의 B → w, d
-- ---------------------------------------------------
SELECT B
FROM R1
WHERE C IN (SELECT C FROM R2 WHERE D = 'p');
-- 답:  B
--      w
--      d

-- ---------------------------------------------------
-- [문제 3] > ALL 서브쿼리 빈칸 (2022-2 유형 변형)
-- "K사의 모든 단가보다 높은" = > ALL (서브쿼리)
-- ★ ALL: 전부보다 (최댓값보다 큼) / ANY: 하나라도 (최솟값보다 큼)
-- ---------------------------------------------------
SELECT 제품명, 단가, 제조사
FROM 제품
WHERE 단가 > ALL (SELECT 단가 FROM 제품 WHERE 제조사 = 'K');
-- 빈칸 답: ALL

-- ---------------------------------------------------
-- [문제 4] JOIN + AVG 서브쿼리 카운트 (2026-1 유형 변형)
-- <WORKER>가 <TEAM>에 속하고, 예산이 "평균 초과"인 팀의 인원 수
--
-- <TEAM>                       <WORKER>
--  tid  budget                  wid  tid
--  T1   200                     1    T1
--  T2   400                     2    T1
--  T3   600                     3    T2
--  T4   800                     4    T3
--                               5    T3
--                               6    T4
-- ① 서브쿼리: AVG(budget) = (200+400+600+800)/4 = 500
-- ② budget > 500 인 팀: T3, T4
-- ③ 그 팀 소속 WORKER: 4, 5, 6 → COUNT = 3
-- ---------------------------------------------------
SELECT COUNT(*) FROM WORKER w JOIN TEAM t
ON w.tid = t.tid
WHERE t.budget > (SELECT AVG(budget) FROM TEAM);
-- 답: 3

-- ---------------------------------------------------
-- [문제 5] 이중 중첩 + GROUP BY + HAVING (2024-3 유형 변형 - 최고난도)
-- 안쪽부터 3단계로 벗겨낸다.
--
-- <worker>                        <task>
--  no  name  task_id              task_id  tname
--  1   A     100                  100      Web
--  2   B     200                  200      App
--  3   C     100                  300      DB
--  4   D     300                  300      AI    ← task_id 중복 주의!
--
-- ① 가장 안쪽: worker를 task_id로 GROUP BY, 인원 2명 미만
--    100→2명(탈락), 200→1명, 300→1명  → { 200, 300 }
-- ② 중간: task에서 task_id가 200,300인 tname → { App, DB, AI }
-- ③ 바깥: worker JOIN task 후 tname이 ②에 포함된 행 수
--    B-App(1) / D-DB(1) / D-AI(1)   ← D는 300이 두 번 조인됨!
-- ---------------------------------------------------
SELECT count(*)
FROM worker AS w JOIN task AS t ON w.task_id = t.task_id
WHERE t.tname IN (
    SELECT tname FROM task WHERE task_id IN (
        SELECT task_id FROM worker GROUP BY task_id HAVING count(*) < 2
    )
);
-- 답: 3

