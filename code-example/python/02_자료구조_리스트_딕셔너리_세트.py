# ---------------------------------------------------
# [문제 1] 리스트 메서드 추적 (빈출)
# append: 끝에 추가 / insert(위치, 값) / remove(값): 값으로 삭제
# pop(): 마지막을 "꺼내서 반환" / pop(i): i번째를 꺼냄
# ---------------------------------------------------
a = [1, 2, 3]
a.append(4)                     # [1, 2, 3, 4]
a.insert(1, 9)                  # [1, 9, 2, 3, 4]
a.remove(2)                     # [1, 9, 3, 4]   (값 2를 삭제, 인덱스 아님!)
last = a.pop()                  # last=4, a=[1, 9, 3]
print(a, last)                  # [1, 9, 3] 4

# ---------------------------------------------------
# [문제 2] append vs extend (함정 비교)
# append([4,5]) → 리스트 "통째로" 한 요소로 들어감
# extend([4,5]) → 요소를 "풀어서" 이어붙임
# ---------------------------------------------------
p = [1, 2, 3]
q = [1, 2, 3]
p.append([4, 5])
q.extend([4, 5])
print(p, len(p))                # [1, 2, 3, [4, 5]] 4
print(q, len(q))                # [1, 2, 3, 4, 5] 5

# ---------------------------------------------------
# [문제 3] 정렬: sort vs sorted (빈출 비교)
# sort()   : 원본 자체를 정렬, 반환값 None
# sorted() : 정렬된 "새 리스트"를 반환, 원본 유지
# reverse=True 옵션으로 내림차순
# ---------------------------------------------------
n1 = [3, 1, 2]
n2 = [3, 1, 2]
r = sorted(n1)                  # n1은 그대로
n2.sort(reverse=True)           # n2 자체가 내림차순으로
print(n1, r)                    # [3, 1, 2] [1, 2, 3]
print(n2)                       # [3, 2, 1]

# ---------------------------------------------------
# [문제 4] 리스트 슬라이싱과 내장 함수
# sum, max, min, len은 실기 계산 문제의 기본 도구
# ---------------------------------------------------
d = [5, 10, 15, 20, 25]
print(d[1:4])                   # [10, 15, 20]
print(sum(d), max(d), min(d), len(d))   # 75 25 5 5
print(sum(d[::2]))              # 45 (5+15+25, 짝수 인덱스만)

# ---------------------------------------------------
# [문제 5] 2차원 리스트(리스트의 리스트) 기출
# lol[0] → 첫 행 전체, lol[2][1] → 3행의 2번째 요소
# 행마다 길이가 달라도 됨 (Java의 가변 배열과 동일 개념)
# ---------------------------------------------------
lol = [[1, 2, 3], [4, 5], [6, 7, 8, 9]]
print(lol[0])                   # [1, 2, 3]
print(lol[2][1])                # 7
for sub in lol:
    for item in sub:
        print(item, end=' ')    # 1 2 3 4 5 6 7 8 9
    print()                     # 행마다 줄바꿈 → 3줄 출력

# ---------------------------------------------------
