# 요소 1개짜리 튜플은 (5,) 처럼 콤마 필수
# ---------------------------------------------------
tp = (10, 20, 30)
print(tp[1], len(tp))           # 20 3
# tp[0] = 9                     # TypeError: 튜플은 수정 불가

# ---------------------------------------------------
# [문제 7] 딕셔너리 기본 (빈출)
# 키로 접근, 없는 키에 대입하면 "추가", in은 "키" 검사
# ---------------------------------------------------
score = {'kor': 90, 'eng': 80}
score['math'] = 70              # 추가
score['kor'] = 100              # 수정 (키 중복 불가 → 덮어씀)
print(score['eng'])             # 80
print('kor' in score)           # True  (키 검사)
print(90 in score)              # False (값은 in으로 안 잡힘)
print(list(score.keys()))       # ['kor', 'eng', 'math']
print(sum(score.values()))      # 250 (100+80+70)

# ---------------------------------------------------
# [문제 8] 세트 기출 (2021년: 그대로 출제)
# add: 하나 추가(중복은 무시) / remove: 삭제
# update: 여러 개 합치기(합집합) / 순서 없음 → 출력 순서는 바뀔 수 있음
# 답: {'한국', '중국', '베트남', '홍콩', '태국'} - 순서 무관, "구성"이 정답
# ---------------------------------------------------
asia = {'한국', '중국', '일본'}
asia.add('베트남')
asia.add('중국')                # 이미 있으므로 무시
asia.remove('일본')
asia.update({'한국', '홍콩', '태국'})  # '한국'은 중복이라 무시
print(sorted(asia))             # 정렬해서 확인: ['베트남', '중국', '태국', '한국', '홍콩']
print(len(asia))                # 5

# ---------------------------------------------------
# [문제 9] 세트 연산 (변형 출제)
# | 합집합, & 교집합, - 차집합
# ---------------------------------------------------
s1 = {1, 2, 3, 4}
s2 = {3, 4, 5}
print(s1 & s2)                  # {3, 4}
print(s1 - s2)                  # {1, 2}
print(sorted(s1 | s2))          # [1, 2, 3, 4, 5]

# ---------------------------------------------------
# [문제 10] 리스트로 중복 제거 관용구 (기출 응용)
# set()을 거치면 중복이 사라진다
# ---------------------------------------------------
dup = [1, 2, 2, 3, 3, 3]
print(sorted(set(dup)))         # [1, 2, 3]

