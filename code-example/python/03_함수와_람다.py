# [문제 7] type() 비교 기출 (답: 45)
# a = "100.0" → 문자열 → len("100.0") = 5
# b = 100.0   → 실수   → 어느 조건도 안 맞아 else → 20
# c = (100, 200) → 튜플 → else → 20
# 5 + 20 + 20 = 45
# "100.0"은 따옴표가 있으니 문자열! 이게 함정의 전부다.
# ---------------------------------------------------
def type_func(value):
    if type(value) == type(100):        # int인가?
        return 100
    elif type(value) == type(""):       # 문자열인가?
        return len(value)
    else:
        return 20

va = "100.0"
vb = 100.0
vc = (100, 200)
print(type_func(va) + type_func(vb) + type_func(vc))    # 45

# ---------------------------------------------------
# [문제 8] 재귀 (C 06장과 같은 논리, 파이썬 문법으로)
# f(5) = 5 + f(4) = ... = 5+4+3+2+1 = 15
# ---------------------------------------------------
def rsum(n):
    if n <= 1:
        return n
    return n + rsum(n - 1)

print(rsum(5))                  # 15

# ---------------------------------------------------
# [문제 9] 전역 변수와 global (변형 출제)
# 함수 안에서 대입하면 "새 지역변수"가 생긴다.
# 전역을 바꾸려면 global 선언이 필요.
# ---------------------------------------------------
g = 10

def no_change():
    g = 99                      # 지역변수 g (전역과 남남)

def do_change():
    global g
    g = 99                      # 전역 g를 수정

no_change()
print(g)                        # 10
do_change()
print(g)                        # 99

