# ---------------------------------------------------
# [문제 1] 기본값 매개변수 기출 (답: a = 20 b = 2)
# func(20) → num1=20, num2는 생략 → 기본값 2
# print(콤마 구분)은 인자 사이에 공백 1칸씩
# ---------------------------------------------------
def func(num1, num2=2):
    print('a =', num1, 'b =', num2)

func(20)                        # a = 20 b = 2
func(20, 30)                    # a = 20 b = 30

# ---------------------------------------------------
# [문제 2] 불변 객체 전달 기출 (시나공 예제: 답 9 4.0 / 9 12)
# calc 안에서 x, y를 바꿔도 밖의 a, b는 영향 없음(숫자는 불변)
# 단, return으로 받은 a만 9로 바뀜
# x = 3*3 = 9, y = 12/3 = 4.0 (/는 실수!)
# ---------------------------------------------------
def calc(x, y):
    x *= 3
    y /= 3
    print(x, y)                 # 9 4.0
    return x

a, b = 3, 12
a = calc(a, b)
print(a, b)                     # 9 12 (b는 그대로!)

# ---------------------------------------------------
# [문제 3] 가변 객체(리스트) 전달 - 원본이 바뀐다 (기출)
# 리스트는 주소가 전달되므로 함수 안의 수정이 원본에 반영
# 문제 2와 반드시 짝으로 비교해서 기억할 것
# ---------------------------------------------------
def add_item(lst):
    lst.append(99)

data = [1, 2]
add_item(data)
print(data)                     # [1, 2, 99] (원본이 바뀜!)

# ---------------------------------------------------
# [문제 4] 리스트 뒤집기 + 스텝 슬라이싱 기출 (답: 3)
# func: 양끝을 교환하며 뒤집기 → lst = [6,5,4,3,2,1]
#   (파이썬식 교환 a, b = b, a 는 임시변수 없이 한 줄!)
# lst[::2]  = [6, 4, 2] → 합 12
# lst[1::2] = [5, 3, 1] → 합 9
# 12 - 9 = 3
# ---------------------------------------------------
def reverse_func(lst):
    for i in range(len(lst) // 2):
        lst[i], lst[-i - 1] = lst[-i - 1], lst[i]

lst = [1, 2, 3, 4, 5, 6]
reverse_func(lst)               # 원본이 뒤집힘 (문제 3과 같은 원리)
print(sum(lst[::2]) - sum(lst[1::2]))   # 3

# ---------------------------------------------------
# [문제 5] lambda + map 기출 (답: [101, 102, 103, 104, 105])
# lambda num: num + 100 → 이름 없는 한 줄짜리 함수
# map(함수, 리스트) → 각 요소에 함수 적용, list()로 감싸야 리스트
# ---------------------------------------------------
nums = [1, 2, 3, 4, 5]
nums = list(map(lambda num: num + 100, nums))
print(nums)                     # [101, 102, 103, 104, 105]

# ---------------------------------------------------
# [문제 6] filter와 리스트 컴프리헨션 (변형 출제)
# 둘 다 "조건에 맞는 것만 골라내기" - 같은 결과
# [식 for 변수 in 리스트 if 조건] 문법 자체가 빈칸으로 출제됨
# ---------------------------------------------------
src = [1, 2, 3, 4, 5, 6]
evens1 = list(filter(lambda n: n % 2 == 0, src))
evens2 = [n for n in src if n % 2 == 0]
print(evens1, evens2)           # [2, 4, 6] [2, 4, 6]
print([n * n for n in range(1, 4)])     # [1, 4, 9]

