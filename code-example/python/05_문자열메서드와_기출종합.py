# ---------------------------------------------------
# [문제 1] 문자열 메서드 추적 (빈출 세트)
# ---------------------------------------------------
s = "Hello Python"
print(s.upper())                # HELLO PYTHON
print(s.lower())                # hello python
print(s.find('o'))              # 4  (첫 'o'의 인덱스, 없으면 -1)
print(s.count('o'))             # 2
print(s.replace('Python', 'World'))     # Hello World
print(len(s))                   # 12 (공백 포함!)
print(s)                        # Hello Python (원본 불변 확인)

# ---------------------------------------------------
# [문제 2] split과 join (짝으로 암기)
# split: 문자열 → 리스트 / join: 리스트 → 문자열
# '구분자'.join(리스트) - 구분자가 "앞"에 오는 문법이 함정
# ---------------------------------------------------
csv = "red,green,blue"
parts = csv.split(',')
print(parts)                    # ['red', 'green', 'blue']
print('-'.join(parts))          # red-green-blue
print("A B C".split())          # ['A', 'B', 'C'] (기본은 공백 기준)

# ---------------------------------------------------
# [문제 3] strip / isdigit (변형 출제)
# strip: 양끝 공백 제거 / isdigit: 모두 숫자인지 True/False
# ---------------------------------------------------
print("  exam  ".strip() + "!")     # exam!
print("2026".isdigit(), "20a6".isdigit())   # True False

# ---------------------------------------------------
# [문제 4] input 처리 관용구 (실행 대신 값 대입으로 재현)
# 실제 기출: x, y = input().split('-')
# input()은 "무조건 문자열"을 반환 → 숫자 계산엔 int() 필수 (함정!)
# ---------------------------------------------------
line = "10-20"                      # input('입력 :') 대신
x, y = line.split('-')
print(x + y)                        # 1020 (문자열 연결! 30이 아님)
print(int(x) + int(y))              # 30   (int 변환 후 덧셈)

# ---------------------------------------------------
# [문제 5] 리스트 조작 + 음수 슬라이싱 기출 (시나공 예제 재현)
# a = ['abc123', 'def456', 'ghi789'] 에 x, y 추가 후 'def456' 제거
# → a = ['abc123', 'ghi789', '10', '20']
# a[1][-3:]  → 'ghi789'의 뒤 3글자 = '789'
# a[2][:-3]  → '10'에서 뒤 3글자 뺀 나머지 = '' (2글자뿐이라 빈 문자열)
# sep=',' 이므로 "789," 출력
# range(3, 6) → 3 4 5
# ---------------------------------------------------
a = ['abc123', 'def456', 'ghi789']
a.append(x)                     # '10'
a.append(y)                     # '20'
a.remove('def456')
print(a[1][-3:], a[2][:-3], sep=',')    # 789,
for i in range(3, 6):
    print(i, end=' ')           # 3 4 5
print()

# ---------------------------------------------------
# [문제 6] 종합 추적 1 - 반복문 + 조건 + 누적 (기출 스타일)
# i=1~10 중 3의 배수만 문자열로 이어붙이기
# 3, 6, 9 → "369"
# ---------------------------------------------------
result = ''
for i in range(1, 11):
    if i % 3 == 0:
        result += str(i)
print(result)                   # 369

# ---------------------------------------------------
# [문제 7] 종합 추적 2 - while + 리스트 pop (기출 스타일)
# pop()으로 하나씩 꺼내며 누적. 꺼내는 순서는 "뒤에서부터"!
# 40, 30 꺼냄 → total = 70, 남은 것 [10, 20]
# ---------------------------------------------------
stack = [10, 20, 30, 40]
total = 0
while len(stack) > 2:
    total += stack.pop()
print(total, stack)             # 70 [10, 20]

# ---------------------------------------------------
# [문제 8] 종합 추적 3 - 딕셔너리 집계 (기출 스타일)
# 문자 빈도 세기: "engineering"에서 각 글자 수를 딕셔너리로
# e:3, n:3, g:2, i:2, r:1
# ---------------------------------------------------
word = "engineering"
freq = {}
for ch in word:
    if ch in freq:
        freq[ch] += 1
    else:
        freq[ch] = 1
print(freq['e'], freq['n'], freq['g'])  # 3 3 2

# ---------------------------------------------------
# [문제 9] 종합 추적 4 - 슬라이싱 + 조건 한 방 정리
# data[2:7:2] → 인덱스 2, 4, 6 → [3, 5, 7]
# 그 합 = 15, 15 % 4 = 3
# ---------------------------------------------------
data = [1, 2, 3, 4, 5, 6, 7, 8]
picked = data[2:7:2]
print(picked, sum(picked) % 4)  # [3, 5, 7] 3

