/*
============================================================
 정보처리기사 실기 - C 02. 제어문(반복문/switch) 추적
 (2020~2025 기출 유형 분석 반영 / 예제는 기출 변형)
============================================================
 [기출 분석 결과 - 제어문 단원에서 실제로 나온 것]
  ㆍswitch 전체 fall-through (break 전혀 없음 + default까지)
  ㆍ자릿수 분해: 뒤집기(rev*10+n%10), 2진수 형태 → 10진수
  ㆍ배열 순환 인덱스 n[(i+1)%5]
  ㆍwhile + continue 조합 누적
 제어문 문제는 "추적표"가 전부다: 회차 | 변수 | 조건 판정
============================================================
*/
#include <stdio.h>

int main(void) {
    /* ---------------------------------------------------
       [문제 1] 기본 누적 - 추적표 연습
       --------------------------------------------------- */
    int sum = 0;
    for (int i = 1; i <= 10; i++)
        sum += i;
    printf("%d\n", sum);                    // 55

    /* ---------------------------------------------------
       [문제 2] continue와 break 조합
       i=1(+1) 2(+2) 3(스킵) 4(+4) 5(+5) 6(스킵) 7(+7) 8(break)
       sum = 1+2+4+5+7 = 19
       --------------------------------------------------- */
    sum = 0;
    for (int i = 1; i <= 10; i++) {
        if (i % 3 == 0) continue;
        if (i > 7) break;
        sum += i;
    }
    printf("%d\n", sum);                    // 19

    /* ---------------------------------------------------
       [문제 3] 자릿수 합과 뒤집기 (빈출)
       n%10: 마지막 자리 추출 / n/10: 마지막 자리 제거
       rev = rev*10 + n%10  ← 뒤집기 공식 암기
       --------------------------------------------------- */
    int num = 1234, dsum = 0, rev = 0;
    while (num > 0) {
        dsum += num % 10;
        rev = rev * 10 + num % 10; 
        num /= 10;
    }
    printf("%d %d\n", dsum, rev);           // 10 4321

    /* ---------------------------------------------------
       [문제 4] "2진수 모양의 정수" → 10진수 (기출 변형)
       input = 1011 (10진수 리터럴이지만 각 자리를 2진 비트로 취급)
       di(자리 가중치)를 1, 2, 4, 8로 늘려가며:
       1*1 + 1*2 + 0*4 + 1*8 = 11
       --------------------------------------------------- */
    int input = 1011, di = 1, bsum = 0;
    while (1) {
        if (input == 0) break;
        bsum += (input % 10) * di;
        di *= 2;
        input /= 10;
    }
    printf("%d\n", bsum);                   // 11

    /* ---------------------------------------------------
       [문제 5] switch 전체 fall-through (기출 변형 - 최다 함정)
       break가 하나도 없다! case 2 진입 후 default까지 전부 실행:
       c += 2 (3) → c = 0 → c += 3 (3) → c -= 1 (2)
       중간의 c=0 "재대입"이 앞의 계산을 리셋하는 것까지가 함정.
       --------------------------------------------------- */
    int cc = 1;
    switch (2) {
        case 1: cc += 3;
        case 2: cc += 2;
        case 3: cc = 0;
        case 4: cc += 3;
        default: cc -= 1;
    }
    printf("%d\n", cc);                     // 2

    /* ---------------------------------------------------
       [문제 6] switch 정상 등급 판정 (break 있음)
       85/10 = 8 → case 8 → 'B' → break
       case 10,9 겹침은 "의도된 fall-through"
       --------------------------------------------------- */
    int score = 85;
    char grade;
    switch (score / 10) {
        case 10:
        case 9:  grade = 'A'; break;
        case 8:  grade = 'B'; break;
        case 7:  grade = 'C'; break;
        default: grade = 'F';
    }
    printf("%c\n", grade);                  // B

    /* ---------------------------------------------------
       [문제 7] 중첩 for 실행 횟수
       i=0: 0회, i=1: 1회, i=2: 2회 → 총 3회
       --------------------------------------------------- */
    int cnt = 0;
    for (int i = 0; i < 3; i++)
        for (int j = 0; j < i; j++)
            cnt++;
    printf("%d\n", cnt);                    // 3

    /* ---------------------------------------------------
       [문제 8] 별 찍기 - 경계값(<= vs <) 확인 습관
       --------------------------------------------------- */
    for (int i = 1; i <= 3; i++) {
        for (int j = 1; j <= i; j++)
            printf("*");
        printf("\n");                       // * / ** / ***
    }

    /* ---------------------------------------------------
       [문제 9] do-while: 조건이 거짓이어도 최소 1회
       --------------------------------------------------- */
    int t = 0;
    do {
        t++;
    } while (t < 0);
    printf("%d\n", t);                      // 1

    /* ---------------------------------------------------
       [문제 10] 무한 루프 + break
       (1,1)(2,3)(3,6)(4,10)(5,15) → 15>10에서 탈출
       --------------------------------------------------- */
    int i2 = 0;
    sum = 0;
    while (1) {
        i2++;
        sum += i2;
        if (sum > 10) break;
    }
    printf("%d %d\n", i2, sum);             // 5 15

    /* ---------------------------------------------------
       [문제 11] 콤마로 두 변수 제어하는 for
       (0,10)→(2,9)→(4,8)→(6,7)→(8,6): 8<6 거짓 → 4회 실행
       --------------------------------------------------- */
    int i3, j3, loop = 0;
    for (i3 = 0, j3 = 10; i3 < j3; i3 += 2, j3--)
        loop++;
    printf("%d %d %d\n", loop, i3, j3);     // 4 8 6

    /* ---------------------------------------------------
       [문제 12] 배열 순환 인덱스 (기출 변형)
       n[(i+2) % 5] → i=0..4일 때 인덱스 2,3,4,0,1
       출력: 30 40 50 10 20 ("두 칸 밀어서 출력" 효과)
       --------------------------------------------------- */
    int narr[] = {10, 20, 30, 40, 50};
    for (int i = 0; i < 5; i++)
        printf("%d ", narr[(i + 2) % 5]);   // 30 40 50 10 20
    printf("\n");

    return 0;
}

