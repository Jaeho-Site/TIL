/*
============================================================
 정보처리기사 실기 - Java 01. 기초 문법 출력 추적
 (2020~2025 기출 유형 분석 반영 / 예제는 기출 변형)
 실행: javac -encoding UTF-8 01_기초문법_출력추적.java && java Ex01Basic
============================================================
 [기출 분석 결과 - 기초 단원에서 실제로 나온 것]
  ㆍ문자열 연결 + (매회 출제급)
  ㆍ| & ^(XOR) ! 를 섞은 조건식으로 if 분기 타기
  ㆍ"조건을 만족하는 마지막 값"을 변수에 남기는 반복문
  ㆍ2차원 가변 배열 aa[0].length
 [C와 다른 점만 복기]
  boolean 타입 존재(if(1) 불가) / char 산술 → int 승격
  >>> (부호 없는 시프트)는 Java 전용 / 논리 결과는 true/false
============================================================
*/
class Ex01Basic {
    public static void main(String[] args) {
        /* -----------------------------------------------
           [문제 1] 문자열 연결 + (최다 빈출)
           왼쪽부터 평가, 문자열을 만나는 순간부터 전부 연결
           1+2=3 → "33" → "334" → "3345"
           ----------------------------------------------- */
        System.out.println(1 + 2 + "3" + 4 + 5);    // 3345
        System.out.println("1" + 2 + 3);            // 123
        System.out.println("결과: " + (1 + 2));     // 결과: 3 (괄호 우선)

        /* -----------------------------------------------
           [문제 2] char 산술 - 결과는 int
           ----------------------------------------------- */
        System.out.println('A' + 1);                // 66
        System.out.println((char)('A' + 1));        // B
        System.out.println((char)('a' - 32));       // A

        /* -----------------------------------------------
           [문제 3] 나눗셈/형변환 (C와 동일 규칙)
           ----------------------------------------------- */
        System.out.println(5 / 2);                  // 2
        System.out.println(5 / 2.0);                // 2.5
        System.out.println((double) 5 / 2);         // 2.5 (캐스팅이 / 보다 먼저)
        System.out.println((int) 3.9);              // 3

        /* -----------------------------------------------
           [문제 4] byte 오버플로
           127 + 1 → 범위를 넘어 -128로 순환
           ----------------------------------------------- */
        byte bt = (byte)(127 + 1);
        System.out.println(bt);                     // -128

        /* -----------------------------------------------
           [문제 5] 시프트 (>>> 은 Java 전용)
           ----------------------------------------------- */
        System.out.println(3 << 2);                 // 12
        System.out.println(-8 >> 1);                // -4 (부호 유지)
        System.out.println(-8 >>> 28);              // 15 (0으로 채움)

        /* -----------------------------------------------
           [문제 6] 배열 기본값과 length
           숫자 0 / boolean false / 참조형 null
           ----------------------------------------------- */
        int[] arr = new int[3];
        String[] strs = new String[2];
        System.out.println(arr[0] + " " + strs[0] + " " + arr.length);  // 0 null 3

        /* -----------------------------------------------
           [문제 7] 가변 2차원 배열 (기출 변형)
           행마다 길이가 달라도 된다 - aa[i].length가 답의 핵심
           ----------------------------------------------- */
        int[][] aa = { {45, 50, 75}, {89} };
        System.out.println(aa[0].length);           // 3
        System.out.println(aa[1].length);           // 1
        System.out.println(aa[0][1] + " " + aa[1][0]);  // 50 89

        /* -----------------------------------------------
           [문제 8] | & ^ ! 조건 조합 (기출 변형 - 고난도 단골)
           w=2, x=5, y=2, z=7
           (w==1 | w==y)  → false | true  → true
           !(y > z)       → !(false)      → true
           (1==x ^ y!=z)  → false ^ true  → true   (^는 서로 다르면 true)
           true & true & true → if 블록 진입 → w = x+y = 7
           안쪽 if: (7==x ^ y!=w) → false ^ true → true → w 출력
           ----------------------------------------------- */
        int w = 2, x = 5, y = 2, z = 7;
        if ((w == 1 | w == y) & !(y > z) & (1 == x ^ y != z)) {
            w = x + y;
            if (7 == x ^ y != w)
                System.out.println(w);              // 7
            else
                System.out.println(x);
        } else {
            System.out.println(z);
        }

        /* -----------------------------------------------
           [문제 9] 조건 만족하는 "마지막 값" 남기기 (기출 변형)
           1~199에서 3과 5의 공배수(15의 배수)를 만날 때마다 r 갱신
           → 마지막으로 갱신된 195가 답 (최댓값 찾기 효과)
           ----------------------------------------------- */
        int r = 0;
        for (int i = 1; i < 200; i++) {
            if (i % 3 == 0 && i % 5 == 0)
                r = i;
        }
        System.out.println(r);                      // 195

        /* -----------------------------------------------
           [문제 10] 삼항 + 증감 종합
           a2++ 는 5를 사용 → 5 > 5 거짓 → ++b2 → r2=11
           ----------------------------------------------- */
        int a2 = 5, b2 = 10;
        int r2 = (a2++ > 5) ? b2-- : ++b2;
        System.out.println(r2 + " " + a2 + " " + b2);   // 11 6 11

        /* -----------------------------------------------
           [문제 11] 복합 대입 함정 + 향상된 for
           v *= 2 + 3 → v = v * 5 = 10
           ----------------------------------------------- */
        int v = 2;
        v *= 2 + 3;
        System.out.println(v);                      // 10
        int sum = 0;
        for (int e : new int[]{1, 3, 5, 7})
            sum += e;
        System.out.println(sum);                    // 16
    }
}

