# [문제 7] 얕은 복사 [:]의 내부 리스트 공유 (2026년 기출 변형 - 최고난도)
# ① m = [[x] for x in a] → [[5], [6], [7]]  (값마다 내부 리스트)
# ② b = m[:] → "겉 리스트"만 새로 만들고,
#    안의 내부 리스트들은 m과 같은 객체를 "공유"! (얕은 복사)
# ③ b[i+1] += b[i] → +=는 내부 리스트를 "그 자리에서 연장"
#    → m이 가리키는 같은 객체가 늘어난다!
#    i=0: b[1] += b[0] → [6]에 [5] 연장 → [6, 5]
#    i=1: b[2] += b[1] → [7]에 [6, 5] 연장 → [7, 6, 5]
# ④ m의 각 내부 리스트 길이 합 = 1 + 2 + 3 = 6
#
# ★ 함정의 핵심 비교:
#    b[i+1] += b[i]        → 제자리 연장 → m도 바뀜   → 답 6
#    b[i+1] = b[i+1]+b[i]  → "새 리스트"를 b 칸에 대입 → m 그대로 → 답 3
#    (완전히 분리하려면 copy.deepcopy(m)이 필요)
# ---------------------------------------------------
def share(a):
    m = [[x] for x in a]
    b = m[:]                            # 얕은 복사: 내부 리스트 공유
    for i in range(len(b) - 1):
        b[i + 1] += b[i]                # 제자리 연장 → m에도 반영
    return sum(len(x) for x in m)

print(share([5, 6, 7]))                 # 6

