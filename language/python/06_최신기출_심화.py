# [문제 2] 부분 문자열 개수 세기 (2024년 기출 변형)
# s[i:i+len(p)] : i부터 패턴 길이만큼 잘라서 비교
# s = "ababcab"
#   "ab" → 인덱스 0, 2, 5에서 발견 → 3개
#   "bc" → 인덱스 3에서 발견       → 1개
# ★ 함정: p1에 "bc", p2에 "ab"를 담아 놓고 출력 라벨은 반대로!
#   f'ab{cnt(s,p1)} bc{cnt(s,p2)}' → ab 뒤에 "bc의 개수"가 붙는다
# 변수가 "무엇을 담고 있는지"를 라벨이 아니라 값으로 추적할 것.
# ---------------------------------------------------
def cnt(s, p):
    result = 0
    for i in range(len(s)):
        if s[i:i + len(p)] == p:
            result += 1
    return result

s = "ababcab"
p1 = "bc"
p2 = "ab"
print(f'ab{cnt(s, p1)} bc{cnt(s, p2)}')     # ab1 bc3

