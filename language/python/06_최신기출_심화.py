# [문제 3] dict comprehension + set 연산 시점 함정 (2025년 기출 변형)
# ① d = {i: i*3 for i in lst} → {1: 3, 2: 6, 3: 9}
# ② s = set(d.values())       → {3, 6, 9}   ← 이 순간 "복사"됨!
# ③ lst[0] = 50  → d는 이미 만들어진 뒤라 아무 영향 없음 (함정 1)
# ④ d[1] = 8     → d의 values는 {8, 6, 9}가 됨
#                  하지만 s는 ②에서 복사한 {3, 6, 9} 그대로 (함정 2)
# ⑤ s.add(6)     → 이미 있으므로 무시 (세트는 중복 X)
# ⑥ s & set(d.values()) = {3,6,9} & {8,6,9} = {6, 9}
# ---------------------------------------------------
lst = [1, 2, 3]
d = {i: i * 3 for i in lst}
sv = set(d.values())
lst[0] = 50
d[1] = 8
sv.add(6)
print(sorted(sv & set(d.values())))     # [6, 9]
print(len(sv & set(d.values())))        # 2

