# [문제 5] 클래스로 트리 구성 + 레벨 조건 재귀 (2025년 기출 변형 - 최고난도)
# ① [Node(i) for i in li] : 값마다 Node 객체 생성 (리스트 컴프리헨션)
# ② nodes[(i-1)//2].children.append(nodes[i])
#    → i번 노드의 부모는 (i-1)//2 번 (이진 트리 공식 - 암기!)
#    li = [2, 4, 6, 8, 10] 로 만든 트리:
#            2          ← level 0
#          /   \
#         4     6       ← level 1
#        / \
#       8   10          ← level 2
# ③ calc: "짝수 레벨"의 값만 합산하며 재귀
#    level0: 2(포함) → level1: 4,6(제외) → level2: 8,10(포함)
#    2 + 8 + 10 = 20
# 트리 그림 → 레벨 표시 → 조건에 맞는 노드만 동그라미. 그림이 전부다.
# ---------------------------------------------------
class Node:
    def __init__(self, value):
        self.value = value
        self.children = []

def tree(li):
    nodes = [Node(i) for i in li]
    for i in range(1, len(li)):
        nodes[(i - 1) // 2].children.append(nodes[i])
    return nodes[0]                     # 루트 반환

def calc(node, level=0):
    if node is None:
        return 0
    return (node.value if level % 2 == 0 else 0) + sum(calc(n, level + 1) for n in node.children)

li = [2, 4, 6, 8, 10]
root = tree(li)
print(calc(root))                       # 20

