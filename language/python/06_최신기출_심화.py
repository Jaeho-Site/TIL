# [문제 4] enumerate + 딕셔너리에 튜플 저장 (2025년 기출 변형)
# enumerate(data) → (인덱스, 요소) 쌍을 차례로 반환
# result[idx] = (합, 길이) 튜플을 저장
#   0행 [2, 4]    → (6, 2)
#   1행 [1, 3, 5] → (9, 3)
#   2행 [7]       → (7, 1)
# ★ 답안 서식: 딕셔너리 { }, 튜플 ( ), 콤마·공백까지 그대로!
# ---------------------------------------------------
data = [
    [2, 4],
    [1, 3, 5],
    [7]
]
result = {}
for idx, row in enumerate(data):
    result[idx] = (sum(row), len(row))
print(result)                           # {0: (6, 2), 1: (9, 3), 2: (7, 1)}

