#include <stdio.h>
#include <string.h>

/* ---------------------------------------------------
   [문제 1] static 누적 (기출 변형)
   static은 "초기화 1번, 호출 간 값 유지"
   호출마다 n *= 2 → 반환값: 2, 4, 8, 16, 32
   main의 지역변수와 이름이 같아도 완전히 남남(함정용 들러리)
   --------------------------------------------------- */
int func(void) {
    static int n = 1;   // 첫 호출 때만 1로 초기화
    n *= 2;
    return n;
}

/* [문제 2] static이 없으면 매번 1부터 → 항상 2 반환 */
int func_local(void) {
    int n = 1;
    n *= 2;
    return n;
}

/* ---------------------------------------------------
   [문제 3] 재귀 기본형 - 팩토리얼 / 피보나치
   --------------------------------------------------- */
int fact(int n) {
    if (n <= 1) return 1;
    return n * fact(n - 1);
}
int fib(int n) {
    if (n <= 1) return n;
    return fib(n - 1) + fib(n - 2);
}

/* ---------------------------------------------------
   [문제 4] 홀짝 조건 분기 재귀 (기출 변형)
   짝수면 +n, 홀수면 -n (아래에서 위로 표 채우기)
   f(1)=1
   f(2)=f(1)+2 = 3
   f(3)=f(2)-3 = 0
   f(4)=f(3)+4 = 4
   f(5)=f(4)-5 = -1
   f(6)=f(5)+6 = 5
   --------------------------------------------------- */
int func2(int n) {
    if (n <= 1) return n;
    if (n % 2 == 0) return func2(n - 1) + n;
    else            return func2(n - 1) - n;
}

/* ---------------------------------------------------
   [문제 5] printf 위치와 재귀 - 내려가며/돌아오며 출력
   updown(3): 3 2 1 (내려가며) 1 2 3 (돌아오며)
   --------------------------------------------------- */
void updown(int n) {
    if (n == 0) return;
    printf("%d ", n);
    updown(n - 1);
    printf("%d ", n);
}

/* ---------------------------------------------------
   [문제 6] 재귀 2진수 출력 - 출력이 "돌아오며" 일어남
   bin(13): 13→6→3→1→0(종료), 출력은 1 1 0 1 → 1101
   --------------------------------------------------- */
void bin(int n) {
    if (n == 0) return;
    bin(n / 2);
    printf("%d", n % 2);
}

/* ---------------------------------------------------
   [문제 7] 거듭제곱 함수 (기출 변형)
   base를 exp번 곱한다: mp(3,4) = 81
   ※ 기출에서는 이 함수가 main "아래"에 정의돼 있었다.
     반환형이 int면 구형 컴파일러는 통과시키지만,
     표준적으로는 프로토타입(선언)을 위에 써야 한다.
   --------------------------------------------------- */
int mp(int base, int exp) {
    int res = 1;
    for (int i = 0; i < exp; i++)
        res *= base;
    return res;
}

/* ---------------------------------------------------
   [문제 8] 전역 버퍼를 반환하는 함수 (기출 변형 - 고급 함정)
   setname은 항상 "같은 전역 배열 buf"의 주소를 반환한다.
   → n1, n2가 가리키는 곳이 같으므로 마지막 복사 값이 둘 다!
   --------------------------------------------------- */
char buf[20];
char *setname(const char *s) {
    strcpy(buf, s);
    return buf;         // 항상 같은 주소
}

int main(void) {
    /* [문제 1] 2+4+8+16+32 = 62 */
    int n = 0, sum = 0;
    for (int i = 0; i < 5; i++) {
        n++;                    // 들러리 (func의 n과 무관)
        sum += func();
    }
    printf("%d\n", sum);        // 62

    /* [문제 2] 2*5 = 10 */
    sum = 0;
    for (int i = 0; i < 5; i++)
        sum += func_local();
    printf("%d\n", sum);        // 10

    /* [문제 3] */
    printf("%d\n", fact(5));    // 120
    printf("%d\n", fib(6));     // 8  (0 1 1 2 3 5 8)

    /* [문제 4] */
    printf("%d\n", func2(6));   // 5

    /* [문제 5] */
    updown(3);                  // 3 2 1 1 2 3
    printf("\n");

    /* [문제 6] */
    bin(13);                    // 1101
    printf("\n");

    /* [문제 7] */
    printf("%d\n", mp(3, 4));   // 81

    /* [문제 8] */
    char *n1 = setname("Kim");
    char *n2 = setname("Lee");
    printf("%s %s\n", n1, n2);  // Lee Lee (둘 다 마지막 값!)

    return 0;
}

