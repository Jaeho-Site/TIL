/*
============================================================
 정보처리기사 실기 - C 07. 구조체 / 공용체 / 연결 리스트
 (2020~2025 기출 유형 분석 반영 / 예제는 기출 변형)
============================================================
 [기출 분석 결과 - 구조체 단원에서 실제로 나온 것]
  ㆍ구조체 배열 + 포인터 p++ 이동 후 p->멤버 출력 (단골)
  ㆍ구조체 멤버에 16진수 저장 + 비트마스크(&)     - 2025년 신경향
  ㆍ자기참조 구조체(연결 리스트) 삽입/재연결       - 2025년 신경향
 [기본 암기]
  변수.멤버 / 포인터->멤버,  p->m == (*p).m
  구조체 포인터 +1 = 구조체 "한 덩어리"만큼 이동
  union은 멤버가 메모리 공유, sizeof는 최대 멤버 기준
============================================================
*/
#include <stdio.h>
#include <string.h>

struct product {
    char name[10];
    int  price;
};

typedef struct {
    int id;
    int score;
} Student;

void addScore(Student *sp, int bonus) {
    sp->score += bonus;         // 주소로 받아 원본 수정
}

/* [문제 6]용: 구조체 + 비트마스크 (2025년 유형 변형) */
typedef struct {
    int code[2];
} Pack;

int mask(int v) {
    return v & 0x0F;            // 하위 4비트만 남긴다
}

/* [문제 8]용: 자기참조 구조체 (연결 리스트) */
struct node {
    int v;
    struct node *next;
};

/* [문제 9]용: 배열 스택 (2023년 기출 변형)
   top은 "마지막으로 넣은 위치". 비었을 때 -1
   push: 먼저 올리고 저장(++top) / pop: 꺼내고 내림(top--) */
int stk[5];
int top = -1;
void push(int v) { stk[++top] = v; }
int pop(void)    { return stk[top--]; }

/* [문제 10]용: 원형 큐 (2025년 기출 변형)
   rear: 넣을 위치, front: 꺼낼 위치. % SIZE로 끝에서 처음으로 순환 */
#define QSIZE 3
typedef struct {
    int a[QSIZE];
    int front;
    int rear;
} Queue;
void enq(Queue *q, int val) {
    q->a[q->rear] = val;
    q->rear = (q->rear + 1) % QSIZE;
}
int deq(Queue *q) {
    int val = q->a[q->front];
    q->front = (q->front + 1) % QSIZE;
    return val;
}

union share {
    char c;
    int  i;
};

int main(void) {
    /* ---------------------------------------------------
       [문제 1] 점(.)과 화살표(->) 접근
       --------------------------------------------------- */
    struct product one = {"Cup", 25};
    struct product *ip = &one;
    printf("%s %d\n", one.name, one.price);     // Cup 25
    printf("%s %d\n", ip->name, (*ip).price);   // Cup 25 (동일 표기)

    /* ---------------------------------------------------
       [문제 2] 구조체 배열 + 포인터 이동 (기출 변형)
       p = list → list[0], p += 2 → list[2]("Bag")
       (p-1)->price → list[1]의 1200
       포인터 +1은 멤버 단위가 아니라 "구조체 통째"!
       --------------------------------------------------- */
    struct product list[3] = {{"Pen", 500}, {"Note", 1200}, {"Bag", 9000}};
    struct product *p = list;
    p += 2;
    printf("%s %d\n", p->name, p->price);   // Bag 9000
    printf("%d\n", (p - 1)->price);         // 1200

    /* ---------------------------------------------------
       [문제 3] 구조체 배열 순회 - 합/평균 (정수 나눗셈 주의)
       500 + 1200 + 9000 = 10700, 10700/3 = 3566 (버림!)
       --------------------------------------------------- */
    int sum = 0;
    for (int i = 0; i < 3; i++)
        sum += list[i].price;
    printf("%d %d\n", sum, sum / 3);        // 10700 3566

    /* ---------------------------------------------------
       [문제 4] typedef 구조체 + 함수로 원본 수정
       --------------------------------------------------- */
    Student st = {1001, 80};
    printf("%d %d\n", st.id, st.score);     // 1001 80
    addScore(&st, 15);
    printf("%d\n", st.score);               // 95

    /* ---------------------------------------------------
       [문제 5] 구조체 배열 반복 대입 (기출 변형)
       st2[i].n = i, st2[i].g = i+1
       st2[0].n + st2[1].g = 0 + 2 = 2
       --------------------------------------------------- */
    struct A { int n; int g; } st2[2];
    for (int i = 0; i < 2; i++) {
        st2[i].n = i;
        st2[i].g = i + 1;
    }
    printf("%d\n", st2[0].n + st2[1].g);    // 2

    /* ---------------------------------------------------
       [문제 6] 구조체 + 16진수 + 비트마스크 (2025년 유형 변형)
       0x1A = 0001 1010, 0x2F = 0010 1111, 마스크 0x0F = 0000 1111
       0x1A & 0x0F = 1010 = 10
       0x2F & 0x0F = 1111 = 15
       합 = 25
       "16진수 → 2진수 펼치기 → & 연산" 3단계로 푼다.
       --------------------------------------------------- */
    Pack pk = {{0x1A, 0x2F}};
    printf("%d\n", mask(pk.code[0]) + mask(pk.code[1]));    // 25

    /* ---------------------------------------------------
       [문제 7] 공용체(union) - 메모리 공유
       u.i = 65 → 같은 메모리를 char로 읽으면 하위 1바이트 'A'
       sizeof(union) = 가장 큰 멤버(int) = 4
       --------------------------------------------------- */
    union share u;
    u.i = 65;
    printf("%c %d\n", u.c, u.i);            // A 65
    printf("%zu\n", sizeof(union share));   // 4

    /* ---------------------------------------------------
       [문제 8] 연결 리스트 (2025년 신경향 - 반드시 이해)
       n1(10) → n2(20) → n3(30) → NULL 을 next로 연결.
       순회 패턴: for(cur=head; cur != NULL; cur = cur->next)

       ① 전체 순회: 10 20 30
       ② 중간 노드 건너뛰기(삭제와 같은 효과):
          n1.next = n2.next;  → n1이 n3을 직접 가리킴
          다시 순회: 10 30
       삽입/삭제 문제는 "화살표 그림"을 다시 그리는 것이 전부다.
       --------------------------------------------------- */
    struct node n3 = {30, NULL};
    struct node n2 = {20, &n3};
    struct node n1 = {10, &n2};
    struct node *head = &n1;
    struct node *cur;
    for (cur = head; cur != NULL; cur = cur->next)
        printf("%d ", cur->v);              // 10 20 30
    printf("\n");
    n1.next = n2.next;                      // 20을 건너뛰도록 재연결
    for (cur = head; cur != NULL; cur = cur->next)
        printf("%d ", cur->v);              // 10 30
    printf("\n");

    /* ---------------------------------------------------
       [문제 9] 배열 스택 - LIFO (2023년 기출 변형)
       push 10, 20, 30 → [10, 20, 30]
       pop() → 30 꺼냄 → [10, 20]
       push 40 → [10, 20, 40]
       pop(), pop() → 40, 20
       "나중에 넣은 것이 먼저 나온다" - top 위치를 표로 추적
       --------------------------------------------------- */
    push(10);
    push(20);
    push(30);
    pop();                                  // 30 버려짐
    push(40);
    int p1 = pop();                         // 40
    int p2 = pop();                         // 20
    printf("%d %d\n", p1, p2);              // 40 20
    /* ※ printf("%d %d", pop(), pop()) 처럼 한 줄에 쓰면 인수 평가
       순서가 컴파일러마다 달라질 수 있다(gcc는 오른쪽부터).
       시험 답안은 "왼쪽부터"로 가정하고 채점하므로 그렇게 풀면 됨 */

    /* ---------------------------------------------------
       [문제 10] 원형 큐 - FIFO (2025년 기출 변형)
       enq 4  → a[0]=4, rear=1
       enq 9  → a[1]=9, rear=2
       deq    → a[0]=4 꺼냄, front=1
       enq 2  → a[2]=2, rear=0 (3%3=0, 처음으로 순환!)
       deq, deq → 9, 2
       "먼저 넣은 것이 먼저 나온다" + 인덱스가 %로 순환하는 것이 핵심
       --------------------------------------------------- */
    Queue q = {{0}, 0, 0};
    enq(&q, 4);
    enq(&q, 9);
    deq(&q);                                // 4 버려짐 (먼저 넣은 것부터)
    enq(&q, 2);
    int d1 = deq(&q);                       // 9
    int d2 = deq(&q);                       // 2
    printf("%d %d\n", d1, d2);              // 9 2

    return 0;
}

