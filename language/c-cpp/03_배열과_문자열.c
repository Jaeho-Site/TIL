/*
============================================================
 정보처리기사 실기 - C 03. 배열과 문자열
 (2020~2025 기출 유형 분석 반영 / 예제는 기출 변형)
============================================================
 [기출 분석 결과 - 배열/문자열 단원에서 실제로 나온 것]
  ㆍ문자 배열에 값 "삽입 + 뒤로 밀기(시프트)"      - 2025년 신경향
  ㆍ문자열 뒤집은 뒤 홀수 인덱스만 출력            - 함수+배열 조합
  ㆍ두 문자열의 공통 문자 출력 (이중 루프)
  ㆍ문자 - 문자 = 아스키코드 차이 (정수)
 [기본 암기]
  부분 초기화 → 나머지 0 / strlen은 \0 제외, sizeof는 포함
  strcmp는 "같으면 0" / 2차원 배열은 행 우선 저장
============================================================
*/
#include <stdio.h>
#include <string.h>

int main(void) {
    /* ---------------------------------------------------
       [문제 1] 부분 초기화
       a = {1, 2, 0, 0, 0} → a[1]+a[4] = 2
       --------------------------------------------------- */
    int a[5] = {1, 2};
    printf("%d\n", a[1] + a[4]);            // 2

    /* ---------------------------------------------------
       [문제 2] 배열 요소로 배열 채우기 - 대입 순서대로 한 줄씩
       b[0]=1 → b[1]=1+2=3 → b[2]=3+3=6 → 합 10
       --------------------------------------------------- */
    int b[3];
    int s = 0;
    b[0] = 1;
    b[1] = b[0] + 2;
    b[2] = b[1] + 3;
    for (int i = 0; i < 3; i++) s += b[i];
    printf("%d\n", s);                      // 10

    /* ---------------------------------------------------
       [문제 3] 2차원 배열 - 행 우선 저장
       m[1][2]=6, 대각선 m[0][0]+m[1][1] = 1+5 = 6
       --------------------------------------------------- */
    int m[2][3] = {{1, 2, 3}, {4, 5, 6}};
    printf("%d %d\n", m[1][2], m[0][0] + m[1][1]);  // 6 6

    /* ---------------------------------------------------
       [문제 4] 2차원 배열 채우기 - 행 우선 vs 열 우선
       행 우선(m2[i][j]=k++): 1 2 3 / 4 5 6
       m2[0][2]=3, m2[1][0]=4
       열 우선(m2[j][i]=k++)이었다면 1 3 5 / 2 4 6 이 된다!
       --------------------------------------------------- */
    int m2[2][3], k = 1;
    for (int i = 0; i < 2; i++)
        for (int j = 0; j < 3; j++)
            m2[i][j] = k++;
    printf("%d %d\n", m2[0][2], m2[1][0]);  // 3 4

    /* ---------------------------------------------------
       [문제 5] strlen vs sizeof (최다 빈출)
       "hello": strlen 5(\0 제외), sizeof 6(\0 포함)
       char str2[10]="abc": strlen 3, sizeof 10(선언 크기)
       --------------------------------------------------- */
    char str[] = "hello";
    char str2[10] = "abc";
    printf("%zu %zu\n", strlen(str), sizeof(str));    // 5 6
    printf("%zu %zu\n", strlen(str2), sizeof(str2));  // 3 10

    /* ---------------------------------------------------
       [문제 6] 문자열 함수 3종
       strcpy(대상, 원본) / strcat(대상, 원본) / strcmp 같으면 0
       --------------------------------------------------- */
    char buf[20];
    strcpy(buf, "Data");
    strcat(buf, "Base");
    printf("%s %zu\n", buf, strlen(buf));   // DataBase 8
    printf("%d\n", strcmp("apple", "apple") == 0);  // 1

    /* ---------------------------------------------------
       [문제 7] 순회하며 대문자 변환 (소문자-32)
       '\0'을 만날 때까지: for(i=0; word[i]; i++) 패턴
       --------------------------------------------------- */
    char word[] = "Exam2026";
    for (int i = 0; word[i] != '\0'; i++) {
        if (word[i] >= 'a' && word[i] <= 'z')
            word[i] -= 32;
    }
    printf("%s\n", word);                   // EXAM2026

    /* ---------------------------------------------------
       [문제 8] 문자 배열 삽입 + 시프트 (2025년 유형 변형 - 신경향)
       Data = {'C','B','E','F'}(마지막 칸 비움), c='D'
       ① Data[2]-Data[0] = 'E'(69)-'C'(67) = 2  ← 문자 뺄셈은 정수!
       ② Data[i] > 'D' 인 첫 위치 탐색: C(no) B(no) E(yes) → i=2
       ③ 그 자리에 'D'를 넣고, 밀려난 값들을 한 칸씩 뒤로:
          temp='E', Data[2]='D'
          i=3: temp2='F', Data[3]='E', temp='F'
          i=4: Data[4]='F'
       최종: C B D E F
       --------------------------------------------------- */
    char Data[5] = {'C', 'B', 'E', 'F'};
    char c = 'D';
    int idx, temp, temp2;
    printf("%d\n", Data[2] - Data[0]);      // 2
    for (idx = 0; idx < 5; ++idx) {
        if (Data[idx] > c)
            break;
    }
    temp = Data[idx];
    Data[idx] = c;
    idx++;
    for (; idx < 5; ++idx) {
        temp2 = Data[idx];
        Data[idx] = temp;
        temp = temp2;
    }
    for (idx = 0; idx < 5; idx++)
        printf("%c", Data[idx]);            // CBDEF
    printf("\n");

    /* ---------------------------------------------------
       [문제 9] 뒤집은 뒤 홀수 인덱스만 출력 (기출 변형)
       "PROGRAM" 뒤집기 → "MARGORP"
       인덱스: M(0) A(1) R(2) G(3) O(4) R(5) P(6)
       i=1,3,5 → A G R
       --------------------------------------------------- */
    char rv[] = "PROGRAM";
    int len = strlen(rv);
    for (int i = 0, j = len - 1; i < j; i++, j--) {
        char ch = rv[i];
        rv[i] = rv[j];
        rv[j] = ch;
    }
    for (int i = 1; i < len; i += 2)
        printf("%c", rv[i]);                // AGR
    printf("\n");

    /* ---------------------------------------------------
       [문제 10] 두 문자열의 공통 문자 (기출 변형)
       a2의 각 문자를 b2 전체와 비교해 같으면 출력
       "apple" vs "plum": p(o) p(o) l(o) → ppl
       (같은 문자가 여러 번이면 여러 번 출력되는 점이 함정)
       --------------------------------------------------- */
    char *a2 = "apple";
    char *b2 = "plum";
    for (int i = 0; a2[i] != '\0'; i++)
        for (int j = 0; b2[j] != '\0'; j++)
            if (a2[i] == b2[j])
                printf("%c", a2[i]);        // ppl
    printf("\n");

    /* ---------------------------------------------------
       [문제 11] 특정 문자 개수 + 최댓값/최솟값 (기본기)
       --------------------------------------------------- */
    char text[] = "programming";
    int cnt = 0;
    for (int i = 0; text[i]; i++)
        if (text[i] == 'm') cnt++;
    printf("%d\n", cnt);                    // 2

    int data[6] = {27, 5, 61, 3, 48, 12};
    int max = data[0], min = data[0];
    for (int i = 1; i < 6; i++) {
        if (data[i] > max) max = data[i];
        if (data[i] < min) min = data[i];
    }
    printf("%d %d\n", max, min);            // 61 3

    return 0;
}

