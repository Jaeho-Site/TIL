/*
============================================================
 정보처리기사 실기 - C 04. 포인터 기본 (배열과 포인터)
 (2020~2025 기출 유형 분석 반영 / 예제는 기출 변형)
============================================================
 [기출 분석 결과 - 포인터 기본에서 실제로 나온 것]
  ㆍchar* p = "문자열"; → %s에 p+n, %c에 *(p+n), *p+n 조합
  ㆍ*(ary+0) = 값; 표기 섞어서 배열 채우고 합 구하기
  ㆍp = a + i 로 옮겨가며 인접 요소 차 계산
 [암기 등식]
  a[i] == *(a+i)  /  배열 이름 == &a[0]  /  p+1 = "한 칸"(자료형 크기)
  *p++ : 값 쓰고 포인터 이동  /  (*p)++ : 값 증가  /  *++p : 이동 후 값
============================================================
*/
#include <stdio.h>

void swap(int *x, int *y) {     /* 주소를 받아 원본을 교환 */
    int tmp = *x;
    *x = *y;
    *y = tmp;
}

/* [문제 4-2]용: "값"을 받는 swap - 복사본만 바뀌고 원본은 그대로!
   (2024년 기출 변형 - swap을 호출해놓고 아무 일도 안 일어나는 함정) */
void swapVal(int x, int y) {
    int tmp = x;
    x = y;
    y = tmp;
}

int main(void) {
    /* ---------------------------------------------------
       [문제 1] 포인터와 배열의 등식 확인
       --------------------------------------------------- */
    int a[5] = {10, 20, 30, 40, 50};
    int *p = a;
    printf("%d %d %d\n", *p, *(p + 3), p[2]);   // 10 40 30
    printf("%d\n", *(a + 4));                   // 50

    /* ---------------------------------------------------
       [문제 2] *p++ / (*p)++ / *++p 3형제 (최다 빈출)
       p는 a[2](=30)에서 시작
       ① *p++   → 30 출력 후 p는 a[3]으로
       ② (*p)++ → 40 출력 후 a[3]=41
       ③ *p     → 41
       ④ *++p   → p를 a[4]로 옮긴 뒤 50
       --------------------------------------------------- */
    p = a + 2;
    printf("%d\n", *p++);                   // 30
    printf("%d\n", (*p)++);                 // 40
    printf("%d\n", *p);                     // 41
    printf("%d\n", *++p);                   // 50

    /* ---------------------------------------------------
       [문제 3] 포인터끼리 빼면 "칸 수" (바이트 아님)
       p는 a[4], q는 a[1] → 3
       --------------------------------------------------- */
    int *q = a + 1;
    printf("%td\n", p - q);                 // 3

    /* ---------------------------------------------------
       [문제 4] swap - call by address
       --------------------------------------------------- */
    int x = 3, y = 7;
    swap(&x, &y);
    printf("%d %d\n", x, y);                // 7 3

    /* ---------------------------------------------------
       [문제 4-2] 값 전달 swap 함정 (기출 변형)
       swapVal은 복사본을 교환할 뿐 → 원본 11, 19 그대로!
       기출에서는 이 뒤에 switch를 붙여 "swap이 됐다고 착각"하게
       만든다. swap류 함수는 매개변수에 *가 있는지부터 확인.
       --------------------------------------------------- */
    int va = 11, vb = 19;
    swapVal(va, vb);
    printf("%d %d\n", va, vb);              // 11 19 (안 바뀜!)

    /* ---------------------------------------------------
       [문제 5] 문자열 상수와 char 포인터 (기출 단골 세트)
       s → "SoftWare"
       %s에 s+4  : 그 위치부터 \0까지 → "Ware"
       %c에 *(s+2): 한 글자 'f'
       *s + 32   : 'S'(83)+32 = 115 = 's' (문자 산술!)
       --------------------------------------------------- */
    char *s = "SoftWare";
    printf("%s\n", s + 4);                  // Ware
    printf("%c\n", *(s + 2));               // f
    printf("%c\n", *s + 32);                // s

    /* ---------------------------------------------------
       [문제 6] 포인터 표기로 배열 채우기 (기출 변형)
       *(v+0) = 2         → v[0]=2
       v[1] = *(v+0) + 3  → 5
       v[2] = *v + 4      → 2+4 = 6   (*v는 v[0])
       합 = 2+5+6 = 13
       --------------------------------------------------- */
    int v[3];
    int vs = 0;
    *(v + 0) = 2;
    v[1] = *(v + 0) + 3;
    v[2] = *v + 4;
    for (int i = 0; i < 3; i++)
        vs += v[i];
    printf("%d\n", vs);                     // 13

    /* ---------------------------------------------------
       [문제 7] 포인터로 인접 요소 차 구하기 (기출 변형)
       arr = {1, 3, 7, 15}
       i=1: p=arr+1, d[0]=3-1=2,  sum = 2+3        = 5
       i=2: p=arr+2, d[1]=7-3=4,  sum += 4+7   → 16
       i=3: p=arr+3, d[2]=15-7=8, sum += 8+15  → 39
       "차이 배열 + 원소"를 같이 누적하는 것이 포인트
       --------------------------------------------------- */
    int arr2[4] = {1, 3, 7, 15};
    int d[3];
    int *pp;
    int sum = 0;
    for (int i = 1; i < 4; i++) {
        pp = arr2 + i;
        d[i - 1] = *pp - arr2[i - 1];
        sum = sum + d[i - 1] + arr2[i];
    }
    printf("%d\n", sum);                    // 39

    /* ---------------------------------------------------
       [문제 8] 센티널(0)까지 포인터 순회 누적
       while(*pt) → 값이 0이면 종료. 2+4+6+8 = 20
       --------------------------------------------------- */
    int nums[] = {2, 4, 6, 8, 0};
    int *pt = nums, total = 0;
    while (*pt) {
        total += *pt;
        pt++;
    }
    printf("%d\n", total);                  // 20

    /* ---------------------------------------------------
       [문제 9] 포인터 이동으로 strlen 구현
       끝 주소 - 시작 주소 = 글자 수
       --------------------------------------------------- */
    char *str = "engineer";
    char *cp = str;
    while (*cp) cp++;
    printf("%td\n", cp - str);              // 8

    return 0;
}

