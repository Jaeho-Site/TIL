-- ---------------------------------------------------
-- [문제 1] COUNT + AND/OR 우선순위 (2021-1→2024-1 재출제 유형 변형)
-- SQL도 C처럼 AND가 OR보다 우선순위가 높다!
-- WHERE A AND B OR C  ==  WHERE (A AND B) OR C
--
-- <STAFF>          ① SNO > 110 AND PAY >= 3500 → (220, 3500) 1건
--  SNO   PAY       ② OR SNO = 330             → (330, 2400) 추가
--  110   1800      ③ 합집합 = 2건
--  220   3500
--  330   2400
-- ---------------------------------------------------
SELECT COUNT(*) FROM STAFF
WHERE SNO > 110 AND PAY >= 3500 OR SNO = 330;
-- 답: 2

-- ---------------------------------------------------
-- [문제 2] 정렬/패턴 빈칸 완성 (2021-2, 2022-1 유형 변형)
-- (a) "가격 기준 내림차순 정렬" → 빈칸 채우기
--     ① ORDER   ② price   ③ DESC
--     (오름차순은 ASC이며 생략 가능 - 내림차순 DESC는 생략 불가!)
-- ---------------------------------------------------
SELECT name, price
FROM 상품
ORDER BY price DESC;        -- ①ORDER ②price ③DESC

-- (b) "제목이 '데'로 시작하는 행을 출판일 내림차순으로"
--     LIKE 와일드카드: %는 0글자 이상, _는 정확히 1글자
--     '데%' 시작 / '%데' 끝 / '%데%' 포함 / '데_' 데+한글자
SELECT * FROM 도서
WHERE 제목 LIKE '데%'        -- ① '데%'
ORDER BY 출판일 DESC;        -- ② DESC

-- ---------------------------------------------------
-- [문제 3] COUNT(속성)의 NULL 제외 (2022-2→2025-3 재출제 유형 변형)
-- ★ COUNT(*)  : 조건을 만족한 "행 수" 전부
-- ★ COUNT(C2) : 그중 C2가 NULL이 아닌 것만! - 이게 함정의 전부
--
-- <TBL>
--  IDX  C1    C2      ① C1 IN (1, 4)  → IDX 1, 3
--  1    1     NULL    ② C2 IN (2, 7)  → IDX 2, 3, 5
--  2    5     7       ③ OR 합집합     → IDX 1, 2, 3, 5 (4행)
--  3    4     2       ④ COUNT(C2): IDX 1의 C2는 NULL → 제외 → 3
--  4    9     4
--  5    NULL  7
-- ---------------------------------------------------
SELECT COUNT(C2)
FROM TBL
WHERE C1 IN (1, 4) OR C2 IN (2, 7);
-- 답: 3   (COUNT(*)였다면 4)

-- ---------------------------------------------------
-- [문제 4] DISTINCT 3종 튜플 수 (2022-3→2026-1 문제·답 동일 재출제의 변형)
-- <EMP> 테이블: 영업부 30명, 개발부 60명, 총무부 30명 (총 120명)
--
-- ① DISTINCT 없음         → 전체 행 수 그대로            → 120
-- ② DISTINCT DEPT         → 중복 제거된 부서 종류         → 3
-- ③ COUNT(DISTINCT DEPT) WHERE DEPT='개발부'
--    → '개발부' 60행에서 중복 제거하면 1종류 → COUNT      → 1
-- ★ 이 문제는 2022-3과 2026-1에 답(200/3/1)까지 똑같이 나왔다.
--    "몇 명이냐"가 아니라 "표시되는 튜플 수"를 묻는 것에 주의!
-- ---------------------------------------------------
SELECT DEPT FROM EMP;                                        -- ① 120
SELECT DISTINCT DEPT FROM EMP;                               -- ② 3
SELECT COUNT(DISTINCT DEPT) FROM EMP WHERE DEPT = '개발부';  -- ③ 1

