-- ---------------------------------------------------
-- [문제 1] 구식(콤마) 등가 조인 결과 (2025-1 유형 변형)
-- FROM A, B WHERE A.key = B.key  ==  INNER JOIN과 동일
--
-- <mem>              <bonus>
--  id    name         id    amt
--  2001  김철수       2001  250
--  2003  박영희       2003  700
--  2005  정민호       2007  900
--  2007  최수진       2009  400
--
-- ① id가 양쪽에 다 있는 것만 짝: 2001, 2003, 2007 (2005, 2009 탈락)
-- ② 그중 amt >= 700: (박영희 700), (최수진 900)
-- ★ 답은 "속성명 포함 표 그대로" 써야 한다 (2025-1 답안 기준):
--      name    amt
--      박영희  700
--      최수진  900
-- ---------------------------------------------------
SELECT name, amt
FROM mem, bonus
WHERE mem.id = bonus.id AND amt >= 700;

-- ---------------------------------------------------
-- [문제 2] LEFT JOIN + ON 빈칸 (2021-2 유형 변형)
-- LEFT JOIN: 왼쪽(a) 테이블은 전부 나오고, 짝 없는 행은 NULL로 채움
--
-- <학생 a>            <동아리 b>
--  학번  이름          학번  동아리명
--  1     민준          1     축구
--  2     서연          3     밴드
--  3     도윤
--
-- 결과: 민준-축구 / 서연-NULL / 도윤-밴드   ← 서연이 살아남는 게 LEFT
-- 빈칸: ① ON   ② 학번   (조인 조건은 WHERE가 아니라 ON!)
-- ---------------------------------------------------
SELECT a.학번, 이름, 동아리명
FROM 학생 a LEFT JOIN 동아리 b
ON a.학번 = b.학번;          -- ①ON ②학번

-- ---------------------------------------------------
-- [문제 3] CROSS JOIN + LIKE 매칭 카운트 (2021-3→2025-3 재출제 유형 변형)
-- CROSS JOIN: 조건 없이 모든 조합 (3행 × 2행 = 6조합)
-- 그 6조합마다 NAME LIKE RULE 검사 → 참인 것만 COUNT
--
-- <A> NAME: KIM, LEE, PARK      <B> RULE: 'K%', '%K'
--
--            'K%'(K로 시작)   '%K'(K로 끝)
--  KIM       ○               ×(M으로 끝)
--  LEE       ×               ×
--  PARK      ×               ○(K로 끝)
--                                          → 참 2개
-- ---------------------------------------------------
SELECT COUNT(*) CNT          -- 'COUNT(*) AS CNT'에서 AS 생략 가능 (기출 포인트)
FROM A CROSS JOIN B
WHERE A.NAME LIKE B.RULE;
-- 답: CNT
--      2

-- ---------------------------------------------------
-- [문제 4] 조인 용어 빈칸 (2024-1 유형 변형) - 서술/용어형
-- ㆍ( 세타 조인 )  : 비교 연산자(=, <, > 등)를 일반적으로 사용한 조인
-- ㆍ( 동등 조인 )  : 그중 = 만 사용한 조인 (중복 속성 그대로 표시)
-- ㆍ( 자연 조인 )  : 동등 조인에서 중복 속성을 한 번만 표시 (NATURAL JOIN)
-- ---------------------------------------------------

