-- ---------------------------------------------------
-- [문제 1] INSERT 전체 작성형 (2023-2 유형 변형)
-- "사원 테이블에 (1024, 강백호, 영업부, 010-2345-6789)를 삽입하는
--  SQL문을 쓰시오" - 전체를 직접 쓰는 문제가 실제로 나왔다!
--
-- ★ 모든 속성에 값을 다 넣으면 속성명 목록 생략 가능
-- ★ 문자값은 따옴표 필수, 숫자는 따옴표 없이
-- ---------------------------------------------------
INSERT INTO 사원 VALUES(1024, '강백호', '영업부', '010-2345-6789');

-- 일부 속성만 넣을 때는 속성명 명시 (나머지는 NULL):
INSERT INTO 사원(사번, 이름) VALUES(1025, '서태웅');

-- ---------------------------------------------------
-- [문제 2] INSERT/SELECT/FROM/SET 4빈칸 세트 (2024-2 유형 변형)
-- 하나의 시나리오로 4개 구문을 완성시키는 최신 유형
--
-- <지원자> 학번(PK) 이름 주소 나이     <수강생> 학번(PK) 이름 나이 과정
-- ---------------------------------------------------

-- [SQL 1] 지원자 정보를 <지원자> 테이블에 추가한다
INSERT INTO 지원자(학번, 이름, 주소, 나이)
VALUES (260315, '한지민', '부산', 22);            -- ① VALUES

-- [SQL 2] 그 지원자를 검색해 <수강생> 테이블에 추가한다
--         ★ INSERT INTO ~ SELECT ~ FROM : 조회 결과를 그대로 삽입
--         ★ '자바과정'처럼 고정 문자열을 SELECT 목록에 끼워 넣을 수 있다
INSERT INTO 수강생(학번, 이름, 나이, 과정)
SELECT 학번, 이름, 나이, '자바과정' FROM 지원자   -- ② SELECT
WHERE 이름 = '한지민';

-- [SQL 3] <수강생> 테이블의 모든 자료를 조회한다
SELECT * FROM 수강생;                             -- ③ FROM

-- [SQL 4] 해당 수강생의 과정 값을 '수료'로 변경한다
UPDATE 수강생 SET 과정 = '수료'                   -- ④ SET
WHERE 학번 = 260315;

-- ---------------------------------------------------
-- [문제 3] UPDATE ~ SET 빈칸 (2021-2 유형 변형)
-- "수강인원이 40 이상인 튜플의 반번호를 505로 갱신"
-- 빈칸 단골: ① UPDATE ② SET  (SET을 빼먹게 유도한다)
-- ---------------------------------------------------
UPDATE 수강반                 -- ① UPDATE
SET 반번호 = 505              -- ② SET
WHERE 수강인원 >= 40;

-- ---------------------------------------------------
-- [문제 4] DELETE 전체 작성형 (2023-1 유형 변형)
-- "회원 테이블에서 이름이 영수인 튜플을 삭제하는 SQL문을 쓰시오"
--
-- ★ DELETE는 FROM이 필수! (DELETE 회원 WHERE ... 는 오답)
-- ★ WHERE 없으면 전체 행 삭제 - 단, 테이블 구조는 남는다
--   (구조까지 제거는 DROP TABLE - DDL과 구분하는 용어 문제 출제)
-- ---------------------------------------------------
DELETE FROM 회원 WHERE 이름 = '영수';

