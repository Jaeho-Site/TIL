 2. UPDATE 짝은 SET, DELETE 짝은 FROM - 빈칸은 이 짝을 노린다
 3. 문자값 따옴표, 숫자값 따옴표 없음 - 전체 작성형 감점 포인트
 4. DELETE(행 삭제, 구조 유지) vs DROP(구조까지 제거) vs
