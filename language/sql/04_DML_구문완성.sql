  ㆍUPDATE ~ SET 빈칸                   - 2021-2
  ㆍDELETE 전체 작성                     - 2023-1
 [DML 3형제 - 용어 문제로도 나옴 (2021-2)]
  INSERT(삽입) / UPDATE(갱신) / DELETE(삭제)  ← DML
  ※ SELECT까지 DML에 포함시키는 분류도 있음 (질의어)
